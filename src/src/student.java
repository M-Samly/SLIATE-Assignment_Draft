    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection con;
            PreparedStatement pst;
            con = db.getConnection();//database connection

            String query = "Insert into student (Name,department,year,email,contact) values (?,?,?,?,?)"; //sql query for insert data to table
            pst = con.prepareStatement(query);

            //set data
            pst.setString(1, txtName.getText().trim());
            pst.setString(2, txtDepartment.getText().trim());
            int year = Integer.parseInt(txtYear.getText().toString().trim());
            pst.setInt(3, year);
            pst.setString(4, txtEmail.getText().trim());
            pst.setString(5, txtContactNo.getText().trim());

            //execute query with data
            pst.executeUpdate();
            //sucess message
            JOptionPane.showMessageDialog(null, "Student Sucessfully Added!");

            //clear old data from textfeild
            clear();

            //refresh table
            DefaultTableModel model = (DefaultTableModel) tblstudentdetials.getModel();
            model.setRowCount(0);
            show_student();

            //curent student id
            sid();
            //keypoint asing to name textfeild
            txtName.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

