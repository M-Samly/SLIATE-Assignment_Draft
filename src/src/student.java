    public ArrayList<studentCode> studentList() {
        ArrayList<studentCode> studentList = new ArrayList<>();
        try {
            Connection con;
            Statement st;
            con = db.getConnection();
            st = con.createStatement();
            String query = "Select * from student order by s_id";
            ResultSet rs = st.executeQuery(query);
            studentCode pro;
            while (rs.next()) {
                pro = new studentCode(rs.getInt("s_id"), rs.getString("name"), rs.getString("department"), rs.getInt("year"), rs.getString("email"), rs.getString("contact"));
                studentList.add(pro);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return studentList;
    }

    public void show_student() {
        ArrayList<studentCode> list = studentList();
        DefaultTableModel model = (DefaultTableModel) tblstudentdetials.getModel();
        Object[] row = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getS_id();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getDepartment();
            row[3] = list.get(i).getYear();
            row[4] = list.get(i).getEmail();
            row[5] = list.get(i).getContact();
            model.addRow(row);
        }
    }

    //get latest student id from database
    public void sid() {
        try {
            Connection con;
            Statement st;
            con = db.getConnection();
            st = con.createStatement();
            String query = "Select s_id from student";
            PreparedStatement pst = con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                int sid = rs.getInt("s_id");
                txtsid.setText(Integer.toString(sid + 1));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

